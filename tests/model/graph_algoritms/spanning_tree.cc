INSTANTIATE_TEST_SUITE_P(spanning_tree, SpanningTreeTest,
                         testing::Range(0, SpanningTreeTest::count_));

TEST_P(SpanningTreeTest, Common) {
  int num_test = this->GetParam();
  SpanningTreeDataTest& data = SpanningTreeTest::test_data_[num_test];

  graph::Graph graph;
  graph.LoadGraphFromFile(data.filename_);

  graph::GraphAlgorithms alg;

  mtlc::Matrix<graph::Graph::EdgeWeightType> spanning_tree =
      alg.GetLeastSpanningTree(graph);

  EXPECT_EQ(spanning_tree.GetCols(), data.size_);

  if (data.size_ > 0) {
    for (size_t i = 0, k = 0; i < spanning_tree.GetRows(); ++i) {
      for (size_t j = 0; j < spanning_tree.GetRows(); ++j, ++k) {
        EXPECT_EQ(spanning_tree(i, j), data.edges_[k]);
      }
    }
  }
}
